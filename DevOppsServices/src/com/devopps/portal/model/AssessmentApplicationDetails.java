package com.devopps.portal.model;

public class AssessmentApplicationDetails {
	
	private int assessmentId;
	private String assessmentAreaName;
	private int applicationId;
	private int weightage;
	private boolean isActive;
	private String applicationName;
	private boolean isEditAllowed;
	
	/**
	 * @return the assessmentId
	 */
	public int getAssessmentId() {
		return assessmentId;
	}
	/**
	 * @param assessmentId the assessmentId to set
	 */
	public void setAssessmentId(int assessmentId) {
		this.assessmentId = assessmentId;
	}
	/**
	 * @return the assessmentAreaName
	 */
	public String getAssessmentAreaName() {
		return assessmentAreaName;
	}
	/**
	 * @param assessmentAreaName the assessmentAreaName to set
	 */
	public void setAssessmentAreaName(String assessmentAreaName) {
		this.assessmentAreaName = assessmentAreaName;
	}
	/**
	 * @return the applicationId
	 */
	public int getApplicationId() {
		return applicationId;
	}
	/**
	 * @param applicationId the applicationId to set
	 */
	public void setApplicationId(int applicationId) {
		this.applicationId = applicationId;
	}
	/**
	 * @return the weightage
	 */
	public int getWeightage() {
		return weightage;
	}
	/**
	 * @param weightage the weightage to set
	 */
	public void setWeightage(int weightage) {
		this.weightage = weightage;
	}
	/**
	 * @return the isActive
	 */
	public boolean isActive() {
		return isActive;
	}
	/**
	 * @param isActive the isActive to set
	 */
	public void setActive(boolean isActive) {
		this.isActive = isActive;
	}
	/**
	 * @return the applicationName
	 */
	public String getApplicationName() {
		return applicationName;
	}
	/**
	 * @param applicationName the applicationName to set
	 */
	public void setApplicationName(String applicationName) {
		this.applicationName = applicationName;
	}
	/**
	 * @return the isEditAllowed
	 */
	public boolean isEditAllowed() {
		return isEditAllowed;
	}
	/**
	 * @param isEditAllowed the isEditAllowed to set
	 */
	public void setEditAllowed(boolean isEditAllowed) {
		this.isEditAllowed = isEditAllowed;
	}
	
	
	
}
