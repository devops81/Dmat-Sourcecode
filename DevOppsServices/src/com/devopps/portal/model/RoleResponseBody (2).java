package com.devopps.portal.model;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "", propOrder = {
		"roleDetails","roleId","statusMsg"})
		
@XmlRootElement(name = "details")
public class RoleResponseBody {
	
	private List<RoleDetails> roleDetails;
	private int roleId;
	private String statusMsg;
	
	/**
	 * @return the subCategoryDetails
	 */
	public List<RoleDetails> getRoleDetails() {
		return roleDetails;
	}
	/**
	 * @param subCategoryDetails the subCategoryDetails to set
	 */
	public void setRoleDetails(
			List<RoleDetails> roleDetails) {
		this.roleDetails = roleDetails;
	}
	
	/**
	 * @return the roleId
	 */
	public int getRoleId() {
		return roleId;
	}
	/**
	 * @param roleId the roleId to set
	 */
	public void setRoleId(int roleId) {
		this.roleId = roleId;
	}
	/**
	 * @return the statusMsg
	 */
	public String getStatusMsg() {
		return statusMsg;
	}
	/**
	 * @param statusMsg the statusMsg to set
	 */
	public void setStatusMsg(String statusMsg) {
		this.statusMsg = statusMsg;
	}
}

