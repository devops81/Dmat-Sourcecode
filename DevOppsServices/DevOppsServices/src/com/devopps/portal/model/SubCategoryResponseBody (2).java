package com.devopps.portal.model;

import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;


@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "", propOrder = {
		"statusMsg","subCategoryDetails","subCategoryId"})
		
@XmlRootElement(name = "details")
public class SubCategoryResponseBody {
	
	private String statusMsg;
	private List<CategorySubCategoryDetails> subCategoryDetails;
	private int subCategoryId;
	
	/**
	 * @return the subCategoryDetails
	 */
	public List<CategorySubCategoryDetails> getSubCategoryDetails() {
		return subCategoryDetails;
	}
	/**
	 * @param subCategoryDetails the subCategoryDetails to set
	 */
	public void setSubCategoryDetails(
			List<CategorySubCategoryDetails> subCategoryDetails) {
		this.subCategoryDetails = subCategoryDetails;
	}
	/**
	 * @return the statusMsg
	 */
	public String getStatusMsg() {
		return statusMsg;
	}
	/**
	 * @param statusMsg the statusMsg to set
	 */
	public void setStatusMsg(String statusMsg) {
		this.statusMsg = statusMsg;
	}
	/**
	 * @return the subCategoryId
	 */
	public int getSubCategoryId() {
		return subCategoryId;
	}
	/**
	 * @param subCategoryId the subCategoryId to set
	 */
	public void setSubCategoryId(int subCategoryId) {
		this.subCategoryId = subCategoryId;
	}
	
	
}