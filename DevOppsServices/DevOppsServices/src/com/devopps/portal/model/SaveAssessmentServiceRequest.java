package com.devopps.portal.model;


public class SaveAssessmentServiceRequest {
	
	private int assessmentId;
	private String assessmentName;
	private int assessmentWeightage;
	/**
	 * @return the assessmentId
	 */
	public int getAssessmentId() {
		return assessmentId;
	}
	/**
	 * @param assessmentId the assessmentId to set
	 */
	public void setAssessmentId(int assessmentId) {
		this.assessmentId = assessmentId;
	}
	/**
	 * @return the assessmentName
	 */
	public String getAssessmentName() {
		return assessmentName;
	}
	/**
	 * @param assessmentName the assessmentName to set
	 */
	public void setAssessmentName(String assessmentName) {
		this.assessmentName = assessmentName;
	}
	/**
	 * @return the assessmentWeightage
	 */
	public int getAssessmentWeightage() {
		return assessmentWeightage;
	}
	/**
	 * @param assessmentWeightage the assessmentWeightage to set
	 */
	public void setAssessmentWeightage(int assessmentWeightage) {
		this.assessmentWeightage = assessmentWeightage;
	}
	
}
